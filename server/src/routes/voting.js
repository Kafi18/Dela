// Голосование по теме
router.post('/vote', requireAuth, async (req, res) => {
  try {
    const userId = req.user.id;
    const { topicId, choice } = req.body; // choice: 'for' | 'against' | 'abstain'

    if (!['for', 'against', 'abstain'].includes(choice)) {
      return res.status(400).json({ message: 'Некорректный вариант голосования' });
    }

    // Один голос на тему: если уже голосовал — не разрешаем повторно
    const existing = await pool.query(
      'SELECT id, choice FROM votes WHERE user_id = $1 AND topic_id = $2',
      [userId, topicId]
    );

    if (existing.rows.length > 0) {
      return res.status(400).json({ message: 'Вы уже проголосовали по этой теме.' });
    }

    const createdAt = new Date().toISOString();
    const inserted = await pool.query(
      `INSERT INTO votes (user_id, topic_id, choice, created_at, updated_at)
       VALUES ($1, $2, $3, $4, $5)
       RETURNING created_at, updated_at`,
      [userId, topicId, choice, createdAt, null]
    );

    if (useEmbeddedDb) {
      // Пишем голос в файл, чтобы он не пропал после перезапуска встроенной БД.
      upsertEmbeddedVote({
        user_email: req.user.email,
        topic_id: topicId,
        choice,
        created_at: inserted.rows[0]?.created_at,
        updated_at: inserted.rows[0]?.updated_at
      });
    }

    res.json({ message: 'Голос учтён' });
  } catch (e) {
    if (isDbUnavailable(e)) {
      return res.json({ message: 'Голос учтён (демо-режим без БД)' });
    }
    console.error(e);
    res.status(500).json({ message: 'Ошибка сервера' });
  }
});

