router.get('/admin/topic/:id/results', requireAuth, requireAdmin, async (req, res) => {
  try {
    const topicId = req.params.id;

    const topic = await pool.query(
      `SELECT t.id, t.title, t.description, m.title AS meeting_title
       FROM topics t
       JOIN meetings m ON m.id = t.meeting_id
       WHERE t.id = $1`,
      [topicId]
    );

    if (topic.rows.length === 0) {
      return res.status(404).json({ message: 'Тема не найдена' });
    }

    const votes = await pool.query(
      `SELECT v.id,
              v.choice,
              u.id AS user_id,
              u.full_name,
              u.email,
              v.created_at,
              v.updated_at
       FROM votes v
       JOIN users u ON u.id = v.user_id
       WHERE v.topic_id = $1
       ORDER BY v.created_at`,
      [topicId]
    );

    const aggregate = await pool.query(
      `SELECT choice, COUNT(*)::int AS count
       FROM votes
       WHERE topic_id = $1
       GROUP BY choice`,
      [topicId]
    );

    res.json({
      topic: topic.rows[0],
      votes: votes.rows,
      aggregate: aggregate.rows
    });
  } catch (e) {
    console.error(e);
    res.status(500).json({ message: 'Ошибка сервера' });
  }
});

