function isDbUnavailable(e) {
  if (e?.code === '28P01' || e?.routine === 'auth_failed') return true;
  if (e?.code === 'ECONNREFUSED' || e?.code === 'ENOTFOUND') return true;
  if (e?.code === '3D000') return true;
  return false;
}

